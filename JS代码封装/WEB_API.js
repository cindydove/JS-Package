/**
 * ???????????????
 * @returns {{left: (Number|number), top: (Number|number)}}
 */
function scroll() {
    return {
        left:window.pageXOffset || document.documentElement.scrollLeft,
        top:window.pageYOffset || document.documentElement.scrollTop
    }
}

